/** 구성 본명성 번호로 심화 해석을 조회합니다. */
export function getGuseongStarDescription(starNumber: number) {
  return GUSEONG_STAR_DESCRIPTIONS[starNumber] ?? null;
}

/** 구성 오행 관계명으로 상세 해석을 조회합니다. */
export function getGuseongRelationDescription(relation: string): string | null {
  // '비화(比和)' → '비화' 로 매칭
  for (const [key, value] of Object.entries(GUSEONG_RELATION_DESCRIPTIONS)) {
    if (relation.includes(key)) return value;
  }
  return null;
}

