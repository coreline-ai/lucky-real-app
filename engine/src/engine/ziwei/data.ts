/** 밝기를 iztro 호환 문자열로 변환 */
export function brightnessToString(b: BrightnessLevel): string {
  const map: Record<BrightnessLevel, string> = {
    '묘': '묘',
    '왕': '왕',
    '득': '득',
    '리': '리',
    '평': '평',
    '불': '불',
    '함': '함',
  };
  return map[b] ?? '';
}

/** 밝기를 수치로 변환 (interpret.ts 호환) */
export function brightnessToScore(b: BrightnessLevel): string {
  const map: Record<BrightnessLevel, string> = {
    '묘': '[+3]',
    '왕': '[+2]',
    '득': '[+1]',
    '리': '[+1]',
    '평': '[0]',
    '불': '[-1]',
    '함': '[-2]',
  };
  return map[b] ?? '[0]';
}

