/**
 * 60갑자 인덱스를 계산한다. (간 인덱스, 지 인덱스로부터)
 */
export function sexagenaryIndex(stemIdx: number, branchIdx: number): number {
  // 60갑자: 간 인덱스 i, 지 인덱스 j 에서
  // sexagenary = (i * 6 - j * 5) mod 60... 아니
  // 표준 공식: s60 where s60 % 10 == stemIdx, s60 % 12 == branchIdx
  // s60 = (stemIdx * 6 + branchIdx * 5) % 60 -- 이것도 아닌데...
  // 정확한 공식: 간/지 모두 짝수 또는 홀수일 때만 유효
  // 간단하게 lookup
  for (let i = 0; i < 60; i++) {
    if (i % 10 === stemIdx && i % 12 === branchIdx) return i;
  }
  return 0;
}

/**
 * 년간+년지로 오행국수(2,3,4,5,6)를 결정한다.
 */
export function getWuxingJu(yearStemIdx: number, yearBranchIdx: number): number {
  const idx = sexagenaryIndex(yearStemIdx, yearBranchIdx);
  return NAYIN_JU_MAP[idx];
}

