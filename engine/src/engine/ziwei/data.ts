// 천부계열(天府星系) 8성: 천부 위치에서 순행으로 배치
// 천부 위치 = 자미 대칭: 천부지지 = (4 - 자미지지 인덱스 + 4) ...
// 사실 천부 위치: 자미가 인(寅) 기준 n칸 순행이면, 천부는 인 기준 n칸 역행
// 즉 천부 = 寅*2 - 자미 = (2*2 - 자미idx + 12) % 12 → (4 - 자미idx + 12) % 12

/**
 * 천부성 위치를 자미성 위치로부터 계산한다.
 * 천부는 자미와 寅(인)궁을 축으로 대칭이다.
 * @param ziweiPos 자미성 지지 인덱스 (BRANCHES 기준)
 * @returns 천부성 지지 인덱스
 */
export function getTianfuPosition(ziweiPos: number): number {
  // 寅(인) = 2
  // 대칭: 천부 = (2 + (2 - ziweiPos) + 12) % 12 = (4 - ziweiPos + 12) % 12
  return ((4 - ziweiPos) % 12 + 12) % 12;
}

