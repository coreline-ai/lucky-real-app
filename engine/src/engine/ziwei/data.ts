export function getWesternSign(month: number, day: number): string {
  // 각 별자리의 시작일
  const signs = [
    { start: [1, 20], end: [2, 18], name: '물병자리' },
    { start: [2, 19], end: [3, 20], name: '물고기자리' },
    { start: [3, 21], end: [4, 19], name: '양자리' },
    { start: [4, 20], end: [5, 20], name: '황소자리' },
    { start: [5, 21], end: [6, 21], name: '쌍둥이자리' },
    { start: [6, 22], end: [7, 22], name: '게자리' },
    { start: [7, 23], end: [8, 22], name: '사자자리' },
    { start: [8, 23], end: [9, 22], name: '처녀자리' },
    { start: [9, 23], end: [10, 22], name: '천칭자리' },
    { start: [10, 23], end: [11, 21], name: '전갈자리' },
    { start: [11, 22], end: [12, 21], name: '사수자리' },
    { start: [12, 22], end: [1, 19], name: '염소자리' },
  ];

  const dayOfYear = month * 100 + day;
  for (const sign of signs) {
    const startDoy = sign.start[0] * 100 + sign.start[1];
    const endDoy = sign.end[0] * 100 + sign.end[1];
    if (sign.name === '염소자리') {
      if (dayOfYear >= startDoy || dayOfYear <= endDoy) return sign.name;
    } else {
      if (dayOfYear >= startDoy && dayOfYear <= endDoy) return sign.name;
    }
  }
  return '염소자리';
}

